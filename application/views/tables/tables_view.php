        <h5 align="center">¿Por qué son importantes las tablas?</h5>
        <p>
        Las tablas se utilizan para el propósito de proporcionar datos tabulares, o para organizar la información en filas y columnas, y no deben ser utilizados a la disposición de los elementos visuales de la página (diseño). El uso de tablas con el propósito de diagramas no sólo influye en el nivel de accesibilidad de un sitio, sino también en la velocidad y el rendimiento al cargar sus páginas. Así que para estilizar y organizar Se aconseja a los aspectos visuales de las páginas de usar hojas de estilo (CSS).</p>

        <p>Muchos dispositivos de tecnología de asistencia proporcionan una funcionalidad de navegación de tabla especial, que permite a los usuarios moverse sobre las celdas de la tabla, con información de encabezado disponible para cada celda</p>
        <p>Algunas etiquetas HTML específicas son necesarias para garantizar que las tablas de datos que son accesibles a los usuarios de lectores de pantalla. Sin estas etiquetas, los usuarios que no pueden ver la tabla pueden encontrar muy difícil o imposible entender las relaciones entre los encabezados de tabla y las celdas dentro de su ámbito. Las etiquetas específicas requeridas dependen de si la tabla es simple o compleja.</p>
        <p>Las tablas accesibles necesitan un marcado HTML que indique las celdas de encabezado y las celdas de datos, y define su relación. Las tecnologías de asistencia utilizan esta información para proporcionar contexto a los usuarios. Para hacer que las tablas sean accesibles, las celdas de encabezado deben estar marcadas con &lt;th&gt;, y las celdas de datos con &lt;td&gt;. Para tablas más complejas, pueden ser necesarias asociaciones explícitas utilizando los atributos scope, id y headers.</p>
        	

