        <h5 align="center">¿Por qué son importantes los menús?</h5>
        <p>
        Los menús son utilizados para facilitar la navegación de los sitios web o para efectuar acciones dentro de la página web, por lo cual estos son una característica que puede mejorar la accesibilidad de un sitio web, ya que si estos menús están bien estructurados permiten las siguientes facilidades:</p>

        <p>1.  Que los lectores de pantalla utilizados por personas con discapacidad visual, detecten todas las opciones del menú y así facilitar el acceso y navegación por los contenidos del sitio web por medio del menú. </p>
        <p>2.  Que el menú pueda ser operado por comandos de teclado para disminuir la cantidad de movimientos que una persona debe de hacer para llegar al contenido que desea ver, esto con el fin de mejorar la navegación por el sitio web a personas con discapacidad motriz.</p>
        <p>3.  Que el menú tenga un tamaño adecuado, esto permite a las personas que usan una pantalla táctil o a personas con discapacidad visual puedan hacer click en la opción que deseaban abrir y disminuya la probabilidad de que abran otra opción que no querían.</p>
       <p> 4.  Que los menús no desaparezcan ya que las personas con enfermedades como párkinson o de manos temblorosas necesitan navegar fuera de los menús y sub-menús sin que estos se oculten después que el mouse ha dejado de estar en el menú.</p>
        <p>5.  Que el menú muestre donde se encuentra el foco es decir en qué parte del menú se encuentra actualmente el usuario, esta característica ayuda a que las personas con memoria a corto plazo u enfermedades como alzhéimer.</p>
	

