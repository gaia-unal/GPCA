        <h5 align="center">¿Por qué son importantes los encabezados?</h5>
        <p>
       Los encabezados demarcan e identifican secciones de un documento o aplicación. Para los usuarios videntes, los encabezados son un medio crítico para escanear rápidamente las secciones principales de un documento, tal vez localizar una sección específica de interés y saltar a ella rápidamente, sin tener que leer todo el documento.
</p>
        <p>HTML proporciona los elementos h1 a h6, cada número indica un nivel de jerarquía. Al igual que los usuarios visuales tienen indicaciones visuales como el tamaño de fuente y el color para identificar los encabezados, los usuarios de tecnología de apoyo se basan en el HTML semántico para hacer lo mismo.</p>
        <p>Muchos dispositivos de tecnología asistencial proporcionan una lista de encabezamientos navegables, ya sea en un árbol o en un formato plano, para que los usuarios puedan escanear rápidamente el documento o la aplicación y saltar a una sección.</p>
        <p>Además los encabezados proporcionan objetivos para que los usuarios puedan saltar de un encabezado a otro solo con oprimir una tecla, ej: en algunos lectores de pantalla es con la letra “H”. </p>
        <p>Los encabezados semánticos son tan útiles que muchos motores de búsqueda los usan para listar los resultados.</p>
