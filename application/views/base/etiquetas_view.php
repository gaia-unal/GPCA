    <div class="art-postcontent"><br>

    <p>Estas son etiquetas HTML que son accesibles y que comúnmente son utilizadas en el desarrollo de sitios web, estas etiquetas por si solas son accesibles ya que las herramientas asistidas las reconocen de manera muy fácil permitiendo el funcionamiento de las mismas.</p>

<table width="791" border="2" align="center">

     <thead>
                <th><div align="center">Etiquetas</div></th>
                 <th>Descripción</th>
</thead>

  <tr>
    <td><div align="center">a</div></td>
    <td><div>La etiqueta &lt;a&gt; se usa para definir un destino o un origen de un enlace.</div>
<div>Los navegadores suelen representar los enlaces subrayando el origen del vínculo de color azul</div></td>  
  </tr>
  <tr>
    <td><div align="center">applet</div></td>
    <td>  <div>Esta etiqueta permite incluir dentro de un documento HTML un applet(subprograma) de Java</div></td>
  </tr>
  <tr>
    <td><div align="center">area</div></td>
    <td>   <div>La etiqueta &lt;area&gt; se utiliza para definir regiones o áreas dentro de un mapa de imágenes y los enlaces asociados a esas regiones.</div><div>
Un mapa de imágenes es una imagen que permite clickear diferentes áreas de la misma.</div>
<div>La etiqueta &lt;area&gt; siempre debe estar ubicada dentro de la etiqueta &lt;map&gt;</div></td>
  </tr>
  <tr>
    <td><div align="center">body</div></td>
    <td>  <div>La etiqueta &lt;body&gt; define el cuerpo de un documento.</div><div>
Dentro del cuerpo del documento se incluye todo el contenido del mismo, por ej. textos, enlaces, imágenes, tablas, etc.</div></td>
  </tr>
  <tr>
    <td><div align="center">button</div></td>
    <td><div>La etiqueta &lt;button&gt; se utiliza para crear botones de un formulario.</div><div>
Los botones creados con esta etiqueta funcionan de igual manera que los botones creados con la etiqueta &lt;input&gt;, pero permiten mejores posibilidades de presentación.</div></td>  
  </tr>
  <tr>
    <td><div align="center">frame</div></td>
    <td>  <div>La etiqueta &lt;frame&gt; define un marco o ventana dentro del elemento frameset.</div><div>

Cada marco puede ser definido con diferentes atributos, dandole así una apariencia distinta, como por ej. colores, bordes, etc.</div></td>
  </tr>
  <tr>
    <td><div align="center">frameset</div></td>
    <td>   <div>La etiqueta &lt;frameset&gt; organiza la ventana principal del usuario por medio del uso de los elementos frame.</div><div>

El elemento frameset contiene dos o más frames.</div><div>

El elemento frameset solo establece la cantidad de columnas o filas que se mostrarán.</div></td>
  </tr>
  <tr>
    <td><div align="center">iframe</div></td>
    <td>  <div>La etiqueta &lt;iframe&gt; permite insertar un marco dentro de otro documento</div></td>
  </tr>
  <tr>
    <td><div align="center">img</div></td>
    <td><div>La etiqueta &lt;img&gt; incluye una imagen en el documento.</div><div>

Esta imagen no es insertada técnicamente  en el documento, sino que se enlaza hacia el mismo.</div></td>  
  </tr>
  <tr>
    <td><div align="center">input</div></td>
    <td>  <div>La etiqueta &lt;input&gt; se utiliza para el ingreso de datos por parte del usuario.</div></td>
  </tr>
  <tr>
    <td><div align="center">object</div></td>
    <td>   <div>La etiqueta &lt;object&gt; se utiliza para insertar objetos tales como imágenes, videos, audio, PDF, Flash, ActiveX y Java applets.</div></td>
  </tr>
  <tr>
    <td><div align="center">select</div></td>
    <td>  <div>La etiqueta &lt;select&gt; crea un menú desplegable.</div></td>
  </tr>
  <tr>
    <td><div align="center">table</div></td>
    <td>  <div>La etiqueta &lt;table&gt; se utiliza para definir una tabla.</div></td>
  </tr>
  <tr>
    <td><div align="center">td</div></td>
    <td>  <div>La etiqueta &lt;td&gt; define una celda que contiene datos dentro de una tabla.</div></td>
  </tr>
  <tr>
    <td><div align="center">textarea</div></td>
    <td>  <div>La etiqueta &lt;textarea&gt; crea un control de entrada de texto multilínea.</div></td>
  </tr>
  <tr>
    <td><div align="center">th</div></td>
    <td>  <div>La etiqueta &lt;th&gt; define una celda de encabezado de una tabla.</div></td>
  </tr>
  
