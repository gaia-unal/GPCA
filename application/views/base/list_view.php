        <h5 align="center">¿Por qué son importantes las listas?</h5>
        <p>
        Las listas ayudan a las tecnologías asistivas para anunciar cuantos elementos hay en un menú y también ofrecen la posibilidad para adaptarse a las necesidades del usuario.</p>
        <h5 align="center">Listas Desordenadas (Unordered list <code>&lt;ul&gt;</code>)</h5>
        <p>Si el sitio web que usted está desarrollando un sitio web en el cual no importa el orden en el que el usuario abre las paginas, se debe de utilizar una lista desordenada &lt;ul&gt;; por lo general los menús de los sitios web no tienen un orden por lo cual este tipo de lista es el más usado. El ejemplo que se muestra a continuación es el caso práctico del menú de esta página, el cual no importa el orden de las opciones ya que el usuario pude acceder a la opción que desee en el orden que quiera.</p>
