    <h5 align="center">Listas Ordenadas (Order list <code>&lt;ol&gt;</code>)</h5>
    <p>Existen casos en los cuales las listas de los menús deben estar ordenadas para que el usuario siga la secuencia de las paginas según como lo desea el propietario de este sitio web, en este caso se debe utilizar las listas ordenadas &lt;ol&gt;, por ejemplo si la página es sobre un libro el menú estará ordenado según los capítulos del libro, en el ejemplo que se muestra a continuación es un caso común de un sitio de cursos de inglés el cual se divide en lecciones.  </p>
