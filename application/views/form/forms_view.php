        <h5 align="center">¿Por qué son importantes los Formularios?</h5>
        <p>
Los formularios son usados comúnmente para proporcionar la interacción de los usuarios con el sitio web, los formularios se utilizan para el registro, inicio de sesión, comentarios, calificaciones, búsquedas y otras actividades en las cuales se precisa de la interacción del usuario con el sitio web.        </p>
        <p>Para crear un formulario en línea accesible, debe asegurarse de que todos los campos de formulario tienen etiquetas o avisos precisos para que los usuarios del lector de pantalla sepan lo que solicita cada campo. Dado que los lectores de pantalla no tienen acceso a las señales visuales que tienen las personas videntes, las etiquetas y los avisos deben asociarse explícitamente con los campos de formulario dentro del HTML. </p>
        <p>
Por esta razón los formularios deben garantizar las siguientes características para que sean accesibles:        </p>
        <p>
1. Los formularios deben ser cortos y claros, esto permite a una persona con una discapacidad no tener que desplazarse mucho por el sitio y dado que para las personas con discapacidad motriz se recomienda que entre menos deba interactuar con un sitio web más accesible es para ellos.       <p> 
2. Los formularios no deben de estar sujetos a tiempo, ya que las persona quieren completar el usuario a su ritmo y si el sistema restringe el formulario puede ocasionar que la persona jamás vuelva a usar la página o a llenar el formulario de nuevo.       </p>
       <p>
3. Todos los elementos del formulario deben de tener un nombre que los identifiquen, dado que un elemento sin nombre puede generar confusión en las personas que usan herramientas asistidas.
       </p>
        <p>
4. Dado que todas las etiquetas de los controles usadas en los formularios son reconocidas por los lectores de pantalla, el uso de los formularios por parte de personas con discapacidad visual o auditiva es muy bueno.        </p>
	

        <p>
5. Los formularios permiten ser llenados por comandos de voz lo cual permite incluir más personas al uso de sitios que presenten materiales en formularios.
        </p>
        
